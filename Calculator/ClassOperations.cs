using System;

namespace Calculator
{
    public class ClassOperations
    {
        public int Add(int a, int b)
        {
            int c = a + b;
            return c;
        }

        public int Sub(int a, int b)
        {
            int c = a - b;
            return c;
        }

        public int Division(int a, int b)
        {
            int c = a / b;
            return c;
        }


        public int Multiplication(int a, int b)
        {
            int c = a * b;
            return c;
        }
    }
}
